# Exemplo 1: Imprimindo mensagens
print("Olá, mundo!")
print("Bem-vindos ao curso de IA!")

# Exemplo 2: Variáveis e tipos de dados
nome = "Maria"              # string (texto)
idade = 25                  # int (número inteiro)
altura = 1.65              # float (número decimal)
esta_matriculado = True     # boolean (verdadeiro/falso)

# Exemplo 3: Imprimindo variáveis
print("Nome:", nome)
print("Idade:", idade)
print("Altura:", altura)
print("Está matriculado?", esta_matriculado)

# Exemplo 4: Concatenação de strings
print("A aluna " + nome + " tem " + str(idade) + " anos.")

# Exemplo 5: f-strings (forma moderna)
print(f"A aluna {nome} tem {idade} anos e {altura}m de altura.")